# 複合代入演算子 +=, -=, *=, /=
num1 = 10
num1 = num1 + 5
print(num1)

number_of_page_in_bocchan = 300;
# number_of_page_in_bocchan = number_of_page_in_bocchan + 10
number_of_page_in_bocchan += 10
print(number_of_page_in_bocchan)

num2 = 10
num2 += 5 # num2 = num2 + 5
print(num2)

x = 100
x -= 20 # x = x - 20
print("x=" + str(x))

x *= 2 # x = x * 2
print("x=" + str(x))

x /= 4 # x = x / 4
print("x=" + str(x))